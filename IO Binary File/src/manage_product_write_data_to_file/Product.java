package manage_product_write_data_to_file;

public class Product {
    private int id;
    private String name;
    private double price;
    private Category category;
    private String description;

    public Product(int id, String name, double price, Category category, String description) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.category = category;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public String toString() {
        return String.format("%-5d,%-20s,%-10f,%-20s,%s\n",this.id,this.name,this.price,this.category,this.description);
    }
}
